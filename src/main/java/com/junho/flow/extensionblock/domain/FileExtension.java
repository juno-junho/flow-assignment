package com.junho.flow.extensionblock.domain;

import lombok.Getter;

@Getter
public enum FileExtension {

    // 고정 확장자
    BAT(new byte[]{}),
    CMD(new byte[]{}),
    COM(new byte[]{(byte) 0xC9}),
    CPL(new byte[]{(byte) 0x4D, (byte) 0x5A}),
    EXE(new byte[]{(byte) 0x4D, (byte) 0x5A}),
    SCR(new byte[]{(byte) 0x4D, (byte) 0x5A}),
    JS(new byte[]{}),

    // 확장자 별 파일 시그니처 (출처 : https://en.wikipedia.org/wiki/List_of_file_signatures
    PNG(new byte[]{(byte) 0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A}),
    JPG(new byte[]{(byte) 0xFF, (byte) 0xD8, (byte) 0xFF}),
    JPEG(new byte[]{(byte) 0xFF, (byte) 0xD8, (byte) 0xFF}),
    GIF(new byte[]{0x47, 0x49, 0x46, 0x38}),
    ZIP(new byte[]{0x50, 0x4B}),
    RAR(new byte[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07}),
    PDF(new byte[]{0x25, 0x50, 0x44, 0x46, 0x2D}),
    MP3(new byte[]{(byte) 0xFF}),
    BMP(new byte[]{0x42, 0x4D}),
    DOCX(new byte[]{0x50, 0x4B}),
    XLSX(new byte[]{0x50, 0x4B}),
    PPTX(new byte[]{0x50, 0x4B}),
    PSD(new byte[]{0x38, 0x42, 0x50, 0x53}),
    WAV(new byte[]{0x52, 0x49, 0x46, 0x46}),
    AVI(new byte[]{0x52, 0x49, 0x46, 0x46}),
    MP4(new byte[]{0x66, 0x74, 0x79, 0x70}),
    FLV(new byte[]{0x46, 0x4C, 0x56}),
    SQLITEDB(new byte[]{0x53, 0x51, 0x4C, 0x69, 0x74, 0x65, 0x20, 0x66}),
    HEIC(new byte[]{0x66, 0x74, 0x79, 0x70, 0x68, 0x65, 0x69, 0x63})
    ;

    private final byte[] signature;

    FileExtension(byte[] signature) {
        this.signature = signature;
    }

}
